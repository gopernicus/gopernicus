package authentication

import (
	"crypto/rand"
	"crypto/sha256"
	"crypto/subtle"
	"encoding/base64"
	"encoding/hex"
	"fmt"
	"math/big"
)

// generateToken generates a random 32-byte token encoded as a 64-character
// hex string. Used for password reset tokens and refresh tokens.
func generateToken() (string, error) {
	b := make([]byte, 32)
	if _, err := rand.Read(b); err != nil {
		return "", fmt.Errorf("auth: generate token: %w", err)
	}
	return hex.EncodeToString(b), nil
}

// hashToken returns the SHA256 hash of a token as a hex string.
// Returns an error for empty input to prevent accidental hash-of-nothing comparisons.
func hashToken(token string) (string, error) {
	if token == "" {
		return "", fmt.Errorf("auth: cannot hash empty token")
	}
	hash := sha256.Sum256([]byte(token))
	return hex.EncodeToString(hash[:]), nil
}

// mustHashToken is a convenience wrapper for hashToken when the input is
// known to be non-empty (e.g., just generated by generateToken or
// generateVerificationCode). Panics on empty input — this indicates a
// programming error, not a runtime condition.
func mustHashToken(token string) string {
	h, err := hashToken(token)
	if err != nil {
		panic(err)
	}
	return h
}

// hashEquals compares two hash strings in constant time.
// Prevents timing side-channels on security-sensitive comparisons.
func hashEquals(a, b string) bool {
	return subtle.ConstantTimeCompare([]byte(a), []byte(b)) == 1
}

// generateVerificationCode creates a cryptographically secure 6-digit
// numeric code for email verification and OTP flows.
func generateVerificationCode() (string, error) {
	n, err := rand.Int(rand.Reader, big.NewInt(1000000))
	if err != nil {
		return "", fmt.Errorf("auth: generate verification code: %w", err)
	}
	return fmt.Sprintf("%06d", n.Int64()), nil
}

// generateBase64URLToken generates a random 32-byte token encoded as a
// base64url string (43 characters, no padding). Used for OAuth PKCE
// verifiers, state parameters, nonces, and flow secrets.
func generateBase64URLToken() (string, error) {
	b := make([]byte, 32)
	if _, err := rand.Read(b); err != nil {
		return "", fmt.Errorf("auth: generate base64url token: %w", err)
	}
	return base64.RawURLEncoding.EncodeToString(b), nil
}

// hashBase64URL returns the SHA256 hash of a value as a base64url string
// (no padding). Used for PKCE flow secret storage.
func hashBase64URL(value string) string {
	if value == "" {
		return ""
	}
	hash := sha256.Sum256([]byte(value))
	return base64.RawURLEncoding.EncodeToString(hash[:])
}
