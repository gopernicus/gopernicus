// TestSatisfierSourcesMatchFramework pins the satisfiers_tmpl.go snapshot to
// the framework's own satisfier sources byte-for-byte. If this fails, a
// satisfier changed under core/auth/*/satisfiers — regenerate the snapshot in
// satisfiers_tmpl.go from the current sources.
func TestSatisfierSourcesMatchFramework(t *testing.T) {
	root, err := testenv.ProjectRoot()
	if err != nil {
		t.Fatalf("resolving framework root: %v", err)
	}

	for rel, want := range satisfierSources {
		got, err := os.ReadFile(filepath.Join(root, filepath.FromSlash(rel)))
		if err != nil {
			t.Errorf("reading framework source %s: %v", rel, err)
			continue
		}
		if string(got) != want {
			t.Errorf("%s drifted from the satisfiers_tmpl.go snapshot — regenerate the snapshot", rel)
		}
	}
}

// TestSatisfierMappingCoversSources pins the feature→file mapping against the
// snapshot in both directions: every mapped path has a source, and every
// snapshotted source is reachable from some feature.
func TestSatisfierMappingCoversSources(t *testing.T) {
	var all []string
	all = append(all, authenticationSatisfierPaths...)
	all = append(all, authorizationSatisfierPaths...)
	all = append(all, invitationsSatisfierPaths...)

	mapped := make(map[string]bool, len(all))
	for _, rel := range all {
		mapped[rel] = true
		if _, ok := satisfierSources[rel]; !ok {
			t.Errorf("mapped satisfier %s has no source in satisfiers_tmpl.go", rel)
		}
	}
	for rel := range satisfierSources {
		if !mapped[rel] {
			t.Errorf("snapshot source %s is not mapped to any feature", rel)
		}
	}
}

func satisfierTestManifest(authentication, authorization bool) *manifest.Manifest {
	m := &manifest.Manifest{Features: &manifest.FeaturesConfig{}}
	if authentication {
		m.Features.Authentication = manifest.FeatureGopernicus
	}
	if authorization {
		m.Features.Authorization = manifest.FeatureGopernicus
	}
	return m
}

func TestEmitSatisfiers(t *testing.T) {
	const module = "github.com/example/testapp"

	t.Run("emits per feature with rewritten repo imports", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(true, true)}

		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("emitSatisfiers: %v", err)
		}

		var all []string
		all = append(all, authenticationSatisfierPaths...)
		all = append(all, authorizationSatisfierPaths...)
		all = append(all, invitationsSatisfierPaths...)

		for _, rel := range all {
			data, err := os.ReadFile(filepath.Join(root, filepath.FromSlash(rel)))
			if err != nil {
				t.Fatalf("expected %s to be emitted: %v", rel, err)
			}
			content := string(data)
			if !strings.HasPrefix(content, "// Code generated by gopernicus. DO NOT EDIT.") {
				t.Errorf("%s missing generated header", rel)
			}
			if strings.Contains(content, `"`+FrameworkModulePath+`/core/repositories/`) {
				t.Errorf("%s still imports framework core/repositories", rel)
			}
			if !strings.Contains(content, `"`+module+`/core/repositories/`) {
				t.Errorf("%s missing project core/repositories import", rel)
			}
		}
	})

	t.Run("authentication only emits no authorization or invitations satisfiers", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(true, false)}

		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("emitSatisfiers: %v", err)
		}
		for _, rel := range append(append([]string{}, authorizationSatisfierPaths...), invitationsSatisfierPaths...) {
			if _, err := os.Stat(filepath.Join(root, filepath.FromSlash(rel))); !os.IsNotExist(err) {
				t.Errorf("%s should not be emitted without authorization", rel)
			}
		}
	})

	t.Run("authorization emits invitations satisfiers too", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(false, true)}

		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("emitSatisfiers: %v", err)
		}
		for _, rel := range append(append([]string{}, authorizationSatisfierPaths...), invitationsSatisfierPaths...) {
			if _, err := os.Stat(filepath.Join(root, filepath.FromSlash(rel))); err != nil {
				t.Errorf("%s should be emitted with authorization: %v", rel, err)
			}
		}
		for _, rel := range authenticationSatisfierPaths {
			if _, err := os.Stat(filepath.Join(root, filepath.FromSlash(rel))); !os.IsNotExist(err) {
				t.Errorf("%s should not be emitted without authentication", rel)
			}
		}
	})

	t.Run("idempotent regenerate rewrites identical bytes", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(true, true)}

		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("first emit: %v", err)
		}
		rel := authenticationSatisfierPaths[0]
		path := filepath.Join(root, filepath.FromSlash(rel))
		first, err := os.ReadFile(path)
		if err != nil {
			t.Fatal(err)
		}
		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("second emit: %v", err)
		}
		second, err := os.ReadFile(path)
		if err != nil {
			t.Fatal(err)
		}
		if string(first) != string(second) {
			t.Errorf("%s not idempotent across regenerates", rel)
		}
	})

	t.Run("skips existing file without generated header", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(true, false)}

		rel := authenticationSatisfierPaths[0]
		path := filepath.Join(root, filepath.FromSlash(rel))
		handWritten := "package satisfiers // user-owned copy\n"
		if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(path, []byte(handWritten), 0644); err != nil {
			t.Fatal(err)
		}

		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("emitSatisfiers: %v", err)
		}
		data, err := os.ReadFile(path)
		if err != nil {
			t.Fatal(err)
		}
		if string(data) != handWritten {
			t.Errorf("hand-written %s was overwritten", rel)
		}

		// Other authentication satisfiers are still emitted.
		other := filepath.Join(root, filepath.FromSlash(authenticationSatisfierPaths[1]))
		if _, err := os.Stat(other); err != nil {
			t.Errorf("expected %s emitted alongside the skip: %v", authenticationSatisfierPaths[1], err)
		}
	})

	t.Run("no features is a no-op", func(t *testing.T) {
		root := t.TempDir()
		cfg := Config{ProjectRoot: root, Manifest: satisfierTestManifest(false, false)}
		if err := emitSatisfiers(cfg, module, Options{}); err != nil {
			t.Fatalf("emitSatisfiers: %v", err)
		}
		if _, err := os.Stat(filepath.Join(root, "core")); !os.IsNotExist(err) {
			t.Error("no files should be written when no features are enabled")
		}
	})
}
